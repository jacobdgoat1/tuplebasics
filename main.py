#Tuple Creation and indexing
fruit_tuple = ("apple", "banana", "cherry", "date")
print(fruit_tuple[0])
print(fruit_tuple[3])
slice_tuple = ([1],[2+1])
print(slice_tuple)
#Tuple Methods
num_tuple = (3, 5, 3, 2, 8, 3, 7)
print(num_tuple.count(3))
print(num_tuple.index(8))
#Tuple Unpacking
person_info = ("Alice", 28, "Engineer")
name, number, boolean = person_info
print(name)
print(number)
print(boolean)

